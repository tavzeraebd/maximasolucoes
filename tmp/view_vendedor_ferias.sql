SELECT 
    PCUSUARI.CODUSUR AS codigoVendedorErp,
    TO_CHAR(PFUFERIASPER.DATAINICIO,'YYYY-MM-DD') AS DATAINICIO,
    TO_CHAR(PFUFERIASPER.DATAFIM,'YYYY-MM-DD') AS DATAFIM
    /*TO_CHAR(PFUFERIASPER.DATAINICIO, 'YYYY-MM-DD') AS DATAINICIO,
    TO_CHAR(PFUFERIASPER.DATAFIM, 'YYYY-MM-DD') AS DATAFIM,
  '[{"codigoVendedorErp": "' || PCUSUARI.CODUSUR || '",'
   || '"dataInicioFerias": "' || TO_CHAR(PFUFERIASPER.DATAINICIO, 'YYYY-MM-DD') || '",'
   || '"dataFimFerias": "' || TO_CHAR(PFUFERIASPER.DATAFIM, 'YYYY-MM-DD') || '"}]' AS ENVIAR
*/FROM 
    RM.PFUNC@DBLINKTSTRM 
    INNER JOIN RM.PPESSOA@DBLINKTSTRM 
        ON PFUNC.NOME = PPESSOA.NOME
    INNER JOIN RM.PFUFERIASPER@DBLINKTSTRM 
        ON PFUFERIASPER.CHAPA = PFUNC.CHAPA 
       AND PFUFERIASPER.CODCOLIGADA = PFUNC.CODCOLIGADA
    INNER JOIN PCUSUARI 
        ON PFUNC.CHAPA = PCUSUARI.CHAPA_RM
WHERE 
    PFUNC.CODFUNCAO = '0357'
    AND PFUNC.CODCOLIGADA IN ('1', '2')
    AND EXTRACT(YEAR FROM PFUFERIASPER.DATAINICIO) = EXTRACT(YEAR FROM SYSDATE)
    AND PFUNC.CODSITUACAO <> 'D'
    AND TRUNC(PFUFERIASPER.DATAINICIO) BETWEEN TRUNC(SYSDATE) AND TRUNC(SYSDATE + 5)
ORDER BY 
    PPESSOA.NOME, 
    PFUFERIASPER.DATAINICIO